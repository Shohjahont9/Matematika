package shohjahont9.mylibrary

class Matematika {
    companion object {
        fun qoshish(num1: Int, num2: Int) = num1 + num2
        fun ayirish(num1: Int, num2: Int) = num1 -  num2
        fun kopaytiruv(num1: Int, num2: Int) = num1 * num2
        fun bolish(num1: Int, num2: Int) = num1 / num2
    }
}